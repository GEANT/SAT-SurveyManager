package org.geant.sat.api.security;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.security.core.Authentication;
import org.springframework.security.web.authentication.AuthenticationSuccessHandler;

/**
 * Success handler setting the OK status code for the response.
 */
public class RestAuthenticationSuccessHandler implements AuthenticationSuccessHandler {

    /** Class logger. */
    private final Logger log = LoggerFactory.getLogger(RestAuthenticationSuccessHandler.class);

    /** {@inheritDoc} */
    @Override
    public void onAuthenticationSuccess(final HttpServletRequest request, final HttpServletResponse response,
            final Authentication authentication) throws ServletException, IOException {
        log.trace("Setting response status to {}", HttpServletResponse.SC_OK);
        response.setStatus(HttpServletResponse.SC_OK);
    }
}